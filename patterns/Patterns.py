#<------------------------------------------------------------------Pattern-6-Start--------------------------------------------------------------------------------->
'''
        *
      * * *
    * * * * *
  * * * * * * *
* * * * * * * * *
  * * * * * * *
    * * * * *
      * * *
        *
'''
n =5
total_rows = 2 * n - 1

for i in range(total_rows):
    for j in range(total_rows):
        if n - 1 - i <= j <= n - 1 + i and i < n:
            print("*", end=" ")
        elif i >= n and i - n + 1 <= j <= total_rows - (i - n + 1) - 1:
            print("*", end=" ")
        else:
            print(" ", end=" ")
    print()
